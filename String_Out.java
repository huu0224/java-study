
public class String_Out {

	 public static void main(String[] args) {

	        // 예제
	        String person =
	                "홍길동은 18세이며 금강산에 있다.";
	        System.out.println(person);

	        // (1) 과자 정보
	        String snack =
	                "새우깡은 1971년에 출시된 농심의 과자이다.";
	        System.out.println(snack);

	        // (2) 영화 정보
	        String movie =
	                "매트릭스는 SF와 액션 장르의 영화로, 감독은 릴리 워쇼스키와 라나 워쇼스키이며 " +
	                "주연배우로는 키아누 리브스, 로렌스 피시번, 캐리 앤 모스, 휴고 위빙이 출연한다.";
	        System.out.println(movie);

	        // (3) 제품 정보
	        String product =
	                "Photoshop은 Adobe System에서 제작한 graphic S/W이다.";
	        System.out.println(product);

	        // (4) 언어 정보
	        String language =
	                "Java는 1995년에 발표된 프로그래밍 언어로, 제작자는 James Gosling이다.";
	        System.out.println(language);
	    }
	}