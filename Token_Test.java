
public class Token_Test {

	public static void main(String[] args) {
		// public[키워드] static[키워드] void[키워드]
	    // main[메소드 이름]
	    // ([분리자] String[클래스 이름] [] [분리자] args[변수 이름] )[분리자]
	    // {[분리자]

	        String name = "홍길동";
	        // String[클래스 이름]
	        // name[변수 이름]
	        // =[연산자]
	        // "홍길동"[문자열 상수]
	        // ;[분리자]

	        int age = 18;
	        // int[키워드]
	        // age[변수 이름]
	        // =[연산자]
	        // 18[정수 상수]
	        // ;[분리자]

	        System.out.println(name);
	        // System[클래스 이름]
	        // .[분리자]
	        // out[변수 이름]
	        // .[분리자]
	        // println[메소드 이름]
	        // ([분리자]
	        // name[변수 이름]
	        // )[분리자]
	        // ;[분리자]

	    }   // }[분리자]

	}       // }[분리자]
